void set_cellvertex(cellvertex *& v, GLfloat* density, GLfloat voxelsize,
	GLuint width, GLuint height,
	GLuint xn, GLuint yn, GLuint zn)
{
	GLuint wh = width*height;//一个平面内的粒子数	
	GLuint base = xn + yn*width + zn*width*height;//当前网格的索引
	//根据密度设置法线方向
	v->n.SetX(-(density[base + 1] - density[base - 1]));
	v->n.SetY(-(density[base + width] - density[base - width]));
	v->n.SetZ(-(density[base + wh] - density[base - wh]));
	//得到当前网格的密度
	v->val = density[base];
	//当前网格的位置
	v->p.SetX(xn*voxelsize);
	v->p.SetY(yn*voxelsize);
	v->p.SetZ(zn*voxelsize);
}

void lerp_vertex(Vector* p, Vector* n,
	cellvertex* v0, cellvertex* v1, GLfloat isolevel)
{
	GLfloat mu;

	if (ABS(isolevel - v0->val) < EPSILON)
	{
		*n = v0->n;
		*p = v0->p;
		return;
	}

	if (ABS(isolevel - v1->val) < EPSILON)
	{
		*n = v1->n;
		*p = v1->p;
		return;
	}

	if (ABS(v0->val - v1->val) < EPSILON)
	{
		*n = v0->n;
		*p = v0->p;
		return;
	}

	mu = (isolevel - v0->val) / (v1->val - v0->val);

	p->SetX(v0->p.getX() + mu * (v1->p.getX() - v0->p.getX()));
	p->SetY(v0->p.getY() + mu * (v1->p.getY() - v0->p.getY()));
	p->SetZ(v0->p.getZ() + mu * (v1->p.getZ() - v0->p.getZ()));

	n->SetX(v0->n.getX() + mu * (v1->n.getX() - v0->n.getX()));
	n->SetY(v0->n.getX() + mu * (v1->n.getX() - v0->n.getX()));
	n->SetZ(v0->n.getZ() + mu * (v1->n.getZ() - v0->n.getZ()));
}

void polygonize(gridcell * grid, GLfloat threshold)
{
	Vector vertlist[12];
	Vector normlist[12];

	int i;
	int cubeindex = 0;
	int edgeFlags = 0;

	if (grid->v[0]->val < threshold) cubeindex |= 1;
	if (grid->v[1]->val < threshold) cubeindex |= 2;
	if (grid->v[2]->val < threshold) cubeindex |= 4;
	if (grid->v[3]->val < threshold) cubeindex |= 8;
	if (grid->v[4]->val < threshold) cubeindex |= 16;
	if (grid->v[5]->val < threshold) cubeindex |= 32;
	if (grid->v[6]->val < threshold) cubeindex |= 64;
	if (grid->v[7]->val < threshold) cubeindex |= 128;

	edgeFlags = edgeTable[cubeindex];

	if (edgeFlags == 0)
		return;
	if (edgeFlags & 1)
		lerp_vertex(&vertlist[0], &normlist[0],
			grid->v[0], grid->v[1], threshold);
	if (edgeFlags & 2)
		lerp_vertex(&vertlist[1], &normlist[1],
			grid->v[1], grid->v[2], threshold);
	if (edgeFlags & 4)
		lerp_vertex(&vertlist[2], &normlist[2],
			grid->v[2], grid->v[3], threshold);
	if (edgeFlags & 8)
		lerp_vertex(&vertlist[3], &normlist[3],
			grid->v[3], grid->v[0], threshold);
	if (edgeFlags & 16)
		lerp_vertex(&vertlist[4], &normlist[4],
			grid->v[4], grid->v[5], threshold);

	if (edgeFlags & 32)
		lerp_vertex(&vertlist[5], &normlist[5],
			grid->v[5], grid->v[6], threshold);

	if (edgeFlags & 64)
		lerp_vertex(&vertlist[6], &normlist[6],
			grid->v[6], grid->v[7], threshold);

	if (edgeFlags & 128)
		lerp_vertex(&vertlist[7], &normlist[7],
			grid->v[7], grid->v[4], threshold);

	if (edgeFlags & 256)
		lerp_vertex(&vertlist[8], &normlist[8],
			grid->v[0], grid->v[4], threshold);

	if (edgeFlags & 512)
		lerp_vertex(&vertlist[9], &normlist[9],
			grid->v[1], grid->v[5], threshold);

	if (edgeFlags & 1024)
		lerp_vertex(&vertlist[10], &normlist[10],
			grid->v[2], grid->v[6], threshold);

	if (edgeFlags & 2048)
		lerp_vertex(&vertlist[11], &normlist[11],
			grid->v[3], grid->v[7], threshold);


	for (i = 0; triTable[cubeindex][i] != -1; i += 3)
	{
		glNormal3fv((GLfloat*)&normlist[triTable[cubeindex][i]]);
		glVertex3fv((GLfloat*)&vertlist[triTable[cubeindex][i]]);

		glNormal3fv((GLfloat*)&normlist[triTable[cubeindex][i + 2]]);
		glVertex3fv((GLfloat*)&vertlist[triTable[cubeindex][i + 2]]);

		glNormal3fv((GLfloat*)&normlist[triTable[cubeindex][i + 1]]);
		glVertex3fv((GLfloat*)&vertlist[triTable[cubeindex][i + 1]]);

	}

}


